package com.practice.maths;

public class MathOperations {
    // This class demonstrates Mathematical operations

    public Integer addition(Integer fist,  Integer second)
    {
        return fist+second;
    }

    public Double subtraction(Double fist, Double second)
    {
        return fist-second;
    }

    public Double multiplication(Double fist,  Double second)
    {
        return fist*second;
    }

    public Double division(Double fist,  Double second)
    {
        return fist/second;
    }
}